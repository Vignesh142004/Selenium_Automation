package vignesh;

public class Relational 
{
  public static void main(String args[]) 
  {
	  /* > ,< ,<=,>=,==,!= */
	int a=20,b=10;
	System.out.println(a>b);//true
	System.out.println(a<b);//false
	System.out.println(a>=b);//true
	System.out.println(a<=b);//false
	System.out.println(a==b);//false
	System.out.println(a!=b);//true
	
	// character camparison
	char c='a',d='b';
	System.out.println(c>d);//false
	System.out.println(c<d);//true
	System.out.println(c>=d);//false
	System.out.println(c<=d);//true
	System.out.println(c==d);//false
	System.out.println(c!=d);//true
  }
}
